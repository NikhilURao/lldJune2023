package constructorchaining;

public class C extends B{

    C() {
        System.out.println("Constructor of C");
    }

    C(int a) {
        System.out.println("Constructor of C with a param");
    }

    C(double a) {
        System.out.println("Constructor of C with a param");
    }

    C(String a) {
        System.out.println("Constructor of C with a param");
    }
}
